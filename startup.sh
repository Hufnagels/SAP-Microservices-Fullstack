#!/usr/bin/env bash
# в”Ђв”Ђ MicroServices вЂ” Startup Script в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
# Starts the full stack step by step using Makefile targets.
# Usage: 
#       ./startup.sh          # full stack
#       ./startup.sh --sim    # + OPC-UA simulator
# Startup order:
# 
# 1. PostgreSQL DBs
# 2. Traefik gateway
# 3. auth-service (first вЂ” others need JWT)
# 4. All app services + RabbitMQ
# 5. Monitoring (InfluxDB в†’ Prometheus/Loki в†’ Grafana)
# 6. OPC-UA simulator (optional, --sim)
# в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

set -euo pipefail

SIM=false
for arg in "$@"; do [[ "$arg" == "--sim" ]] && SIM=true; done

GREEN='\033[0;32m'
CYAN='\033[0;36m'
YELLOW='\033[1;33m'
NC='\033[0m'

step() { echo -e "\n${CYAN}в–¶ $1${NC}"; }
ok()   { echo -e "${GREEN}вњ“ $1${NC}"; }
info() { echo -e "${YELLOW}  $1${NC}"; }

wait_healthy() {
  local name=$1 seconds=${2:-20}
  info "Waiting ${seconds}s for $name..."
  sleep "$seconds"
}

# в”Ђв”Ђ Step 1: Databases в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
step "Starting PostgreSQL databases..."
docker compose up -d postgres-auth postgres-files postgres postgres-maps postgres-labeling postgres-opcua
wait_healthy "PostgreSQL instances" 8
ok "Databases up"

# в”Ђв”Ђ Step 2: Core infrastructure в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
step "Starting Traefik gateway..."
docker compose up -d traefik
sleep 3
ok "Traefik up в†’ http://localhost:8080"

# в”Ђв”Ђ Step 3: Auth service в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
step "Starting auth-service..."
docker compose up --build -d auth-service
wait_healthy "auth-service" 5
ok "Auth service up в†’ /auth"

# в”Ђв”Ђ Step 4: App services в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
step "Starting app services..."
docker compose up --build -d \
  sap-b1-adapter-service \
  file-service \
  binpack-service \
  labeling-service \
  opcua-service \
  orders-service \
  inventory-service \
  reporting-service \
  sensor-ingest-service \
  maps-service \
  rabbitmq
wait_healthy "app services" 10
ok "App services up"

# в”Ђв”Ђ Step 5: Monitoring stack в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
step "Starting monitoring stack (InfluxDB, Prometheus, Loki, Grafana)..."
docker compose up -d influxdb prometheus loki promtail
wait_healthy "InfluxDB + Prometheus" 10
docker compose up -d grafana
sleep 5
ok "Monitoring up в†’ Grafana: http://localhost:3000"

# в”Ђв”Ђ Step 6: OPC-UA simulator (optional) в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
if $SIM; then
  step "Starting OPC-UA simulator..."
  docker compose --profile sim up --build -d opcua-simulator
  sleep 3
  ok "OPC-UA simulator up в†’ opc.tcp://localhost:4840"
fi

# в”Ђв”Ђ Done в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo ""
echo -e "${GREEN}в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ${NC}"
echo -e "${GREEN}  MicroServices stack is up!${NC}"
echo -e "${GREEN}в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ${NC}"
echo ""
echo -e "  Gateway (Traefik):   ${CYAN}http://localhost${NC}"
echo -e "  Traefik dashboard:   ${CYAN}http://localhost:8080${NC}"
echo -e "  Grafana:             ${CYAN}http://localhost:3000${NC}"
echo -e "  InfluxDB:            ${CYAN}http://localhost:8086${NC}"
echo ""
echo -e "  Frontend dev servers (run separately):"
echo -e "    SAP Sync UI:       ${CYAN}make fe-sap      ${NC} в†’ http://localhost:5173"
echo -e "    SAP Map UI:        ${CYAN}make fe-map      ${NC} в†’ http://localhost:5174"
echo -e "    Binpack UI:        ${CYAN}make fe-binpack  ${NC} в†’ http://localhost:5175"
echo -e "    Admin UI:          ${CYAN}make fe-admin    ${NC} в†’ http://localhost:5176"
echo -e "    Live Labeling UI:  ${CYAN}make fe-labeling ${NC} в†’ http://localhost:5178"
echo -e "    S7 Status UI:      ${CYAN}make fe-s7       ${NC} в†’ http://localhost:5179"
echo ""
echo -e "  Status:              ${CYAN}make ps${NC}"
echo -e "  Logs:                ${CYAN}make logs${NC}"
echo ""
